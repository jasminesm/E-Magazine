    Perkembangan Trend Fashion di Indonesia <br>
    <img src="img/gam2.jpg">
    Trend fashion sebagian besar didorong oleh perancang busana yang membuat dan menghasilkan pakaian, Dalam hal ini istilah Bisnis Fashion akan digunakan dalam arti bisnis yang berhubungan dengan pakaian modis atau pakaian sebagai industri kreatif yang diciptakan dan diproduksi oleh perancang busana. Tidak ada yang dapat menyangkal bahwa karya perancang busana memiliki kontribusi besar untuk industri garmen, karena saat ini para pengusaha garmen akan perlu menggunakan keahlian para desainer untuk selalu up to date agar tidak ketinggalan dengan trend fashion yang lagi booming. <br><br>
    Perkembangan trend fashion di Indonesia didorong oleh beberapa faktor yaitu Media Massa, Dunia Entertainment, Dunia Bisnis, dan Internet. Hal ini akan membuat para desainer lebih mudah mengakses dan mengetahui tentang trend fashion yang sedang populer untuk menciptakan variasi dalam fashion. <br><br>

    Media Massa : Media massa selalu menyajikan informasi termasuk, informasi seputar dunia fashion. Melalui kedua media ini, trend fashion seakan disosialisasikan kepada masyarakat dan itulah trend fashion yang harus diikuti. Masyarakat sudah tentu melihat trend fashion yang ditampilkan dalam setiap acara di televisi.<br><br>

    Dunia Entertainment : faktor yang sangat besar dalam menyebarluaskan trend fashion kepada masyarakat. Para selebritas yang selalu muncul di berbagai media dan menjadi idola, selalu berganti mode busana mengikuti trend fashion. Hal ini pasti akan diikuti oleh masyarakat yang menjadi penggemar mereka.<br><br>
