    bagaimana caranya agar pembelajaran secara daring dapat dilakukan secara efektif? Yuk ikuti beberapa tips berikut ini:

    a. Kelola waktu dengan baik : Atur waktu belajar kamu dengan baik, jangan mengulur-ulur waktu dalam mengerjakan tugas. Biasakan fokus dan mengerjakan tugas di awal waktu.

    b. Carilah tempat nyaman : Tempat yang nyaman merupakan salah satu kunci agar belajar tetap fokus, Kamu bisa mencari tempat seperti sudut rumah atau teras yang nyaman untuk belajar, belajar sekreatif mungkin untuk menambah semangat belajar kamu.

    c. Siapkan perangkat yang dibutuhkan : Persiapkan perangkat yang kamu butuhkan, baik itu laptop, gawai, komputer maupun saklar listik untuk mengisi daya perangkat elektronik kamu agar tidak kehabisan daya di tengah-tengah fokus belajar. Pastikan juga perangkat kamu terhubung dengan jaringan internet yang baik agar kamu dapat mengakses Google classroom pada pembelajaran daring yang kamu butuhkan tanpa kendala.

    d. Komunikasi dengan Guru dan teman sekelas : Tetap jaga komunikasi dengan guru dan teman sekelas. Nomor Gawai Bapak/ibu Guru sudah tertera pada Jadwal PJJ yang telah diberikan, jadi Jika kamu butuhkan penjelasan mengenai materi dan tugas dapat langsung menghubungi Bapak/Ibu Guru. Begitu juga, komunikasi dengan teman sekelas itu penting sekali agar kamu bisa berdiskusi mengenai materi atau tugas yang diberikan Bapak/Ibu Guru

    e. Jaga Kebersihan dan Kesehatan : Di saat pandemi COVID-19 ini, kebersihan merupakan salah satu hal yang penting. Rajin mencuci tangan dengan sabun dan tetap jaga kesehatan dengan cara memberikan asupan makan bergizi bagi tubuhmu agar tidak cepat terserang penyakit. So Stay Clean and Stay Healthy Guys! <br>  
