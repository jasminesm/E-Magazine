    Kesehatan merupakan harta yang tak ternilai harganya. Sering sekali cara yang dilakukan menghabiskan banyak biaya
    dan bahkan juga tak jarang menabrak aneka norma dan logika Tetapi, sebenarnya untuk tetap sehat tidaklah sulit, dan
    kadangkala juga tidak memerlukan biaya sama sekali. Tetapi tentunya kita butuh ekstra kesabaran dan ketelatenan,
    karena hanya sedikit orang bisa untuk tetap konsisten menjalani. <br>
    1. Berpikiran Positif (Positive Thinking)
    Kondisi gangguan mental seperti stress dan depresi telah terbukti mengganggu kesehatan tubuh seseorang teruslah
    berpikir positif dan menjaga kondisi mental agar tidak mengalami stress. Pikiran positif penting bagi kehidupan jika
    kita sering berpikiran buruk, energi yang buruk akan menyelimuti tubuh dan memicu stress. <br>
    2. Menjaga Pola Makan
    Mengkonsumsi makananan dengan gizi seimbang yaitu 4 sehat dan 5 sempurna. Semua gizi makanan ada di makanan 4 sehat
    dan 5 sempurna. hindari Junk food atau cepat saji, sebab di dalam makanan itu tidak ada nutrisinya samasekali.
    Justru makanan itu merupakan sumber penyakit bagi tubuh karena makanan mengandung lemak jenuh tinggi menjadi sumber
    penyakit jantung koroner. <br>
    3. Cukupi Kebutuhan Cairan
    Sebuah penelitian menunjukkan jumlah air yang dibutuhkan tergantung pada berbagai faktor Seperti kondisi iklim dan
    cuaca, aktivitas fisik dan juga berat badan. Tetapi umumnya kita perlu 2,7 sampai 3,7 liter asupan air. Atau
    setidaknya kita meminum 8 gelas air per hari guna memenuhi kebutuhan cairan tubuh setiap harinya. Jangan kurang,
    jangan pula berlebihan. Alih-alih ingin mendapatkan hasil maksimal dari manfaat minum air, jangan mengkonsumsi air
    secara berlebihan karena justru akan berdampak buruk bagi kesehatan. <br>
    4. Olahraga Secara Teratur
    Kita bisa berolahraga seperti jogging, bersepeda dan lainnya atau jalan kaki, berjalan kaki mampu membakar sekitar
    180 kalori per menitnya. Semisal ketika ingin pergi ke warung yang jaraknya dekat maka lakukan dengan jalan kaki
    saja, jangan naik motor Selain itu, hindari duduk terlalu lama. Bagi yang tidak memiliki penyakit jantung kita bisa
    berolahraga berat selama 30 menit dalam sehari. Dan untuk orang yang sudah mengalami masalah jantung, untuk cara
    menjaga kesehatan tubuh agar tetap fit bisa melakukan olahraga ringan selama 15 menit dan sesuai kemampuan untuk
    menjaga tubuhnya agar tidak mudah sakit, olahraga itu bisa digunakan untuk melancarkan peredaran darah serta
    membakar lemak yang ada di dalam tubuh. <br>
