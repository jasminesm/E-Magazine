 <h1>SYARAT PENERIMAAN VAKSIN</h1>
    <div><img src="img/vaks.jfif" width="250" height="150"></div> <br>

    Proses vaksinasi untuk menekan penyebaran Covid-19 sudah dimulai dengan Presiden Joko Widodo sebagai orang pertama
    yang menerima vaksin corona. Saat ini, vaksin yang didistribusikan di Indonesia adalah Sinovac, AstraZeneca, dan
    Moderna. Untuk bisa mendapatkannya, ada syarat penerima vaksin yang harus dipenuhi.
    syarat penerima vaksin Covid-19 <br>
    1. Syarat penerima vaksin Sinovac
    Menurut rekomendasi terbaru per tanggal 18 Maret 2021 dari Perhimpunan Dokter Spesialis Penyakit Dalam Indonesia
    (PAPDI) serta imbauan dari Satgas Penanganan Covid-19, berikut ini syarat orang yang boleh dan sebaiknya tidak
    menerima vaksin Covid-19.
    Orang yang boleh menerima vaksin Sinovac yaitu
    a. Berusia >12 tahun dan tidak memiliki riwayat alergi
    b. Tidak sedang demam (≥ 37,5°C) dan tekanan darah kurang dari 180/110mmHg
    c. Pasien pengidap asma, paru, jantung, hipotiroid dan hipertiroid dengan kondisi terkontrol atau stabil
    d. Pasien penyakti ginjal, hati yang kondisinya stabil (mendapat persetujuan dokter)
    e. Ibu hamil, terutama yang tinggal di daerah risiko tinggi penularan Covid-19. Pemberian vaksin pertama dimulai
    pada trimester kedua dan vaksin kedua disesuaikan dengan jarak pemberian vaksin sesuai merek. <br>
    2. Syarat penerima vaksin AstraZeneca
    a. Orang berusia 18 tahun ke atas
    b. Memiliki penyakit kronis bawaan atau komorbid yang terkendali sudah menerima persetujuan dokter yang merawat.
    c. Penyintas Covid-19 yang sudah dinyatakan sembuh setidaknya selama 6 bulan <br>
    3. Syarat penerima vaksin Moderna
    Vaksin Moderna bisa diberikan pada individu berusia di atas 18 tahun. Saat akan menerima vaksin Moderna beritahukan
    kepada petugas apabila Anda memiliki kondisi-kondisi di bawah ini:
    a. Punya riwayat alergi, dan Sedang demam.
    b. Punya riwayat gangguan imun atau sedang mengonsumsi obat yang memengaruhi kerja sistem imun
    c. Sedang hamil atau berencana untuk hamil atau sedang menyusui
    d. Sudah pernah menerima vaksin Covid-19 sebelumnya <br>
    
